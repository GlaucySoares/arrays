A função count() em PHP é usada para contar o número de elementos de um array. 
<br>
Ela retorna um valor inteiro indicando quantos itens existem naquele array.
<br>
<br>


<?php
$frutas = ['Manga', 'Banana', 'Laranja'];

$total = count($frutas);

echo "O array contém $total frutas.";
?>
